package HomeWork3;

public class Calculator {
	public double div (double a,double b){
		double result = a/b;
		return result;
	}
	public double mult (double a,double b){
		double result = a*b;
		return result;
	}
	
	public double sum (double a,double b){
		double result = a+b;
		return result;
	}
	
	public double sub (double a,double b){
		double result = a - b;
		return result;
	}
}
